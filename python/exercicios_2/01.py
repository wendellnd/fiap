'''
Dados 3 lados de um triângulo, calcule e mostre seu perímetro (soma dos lados).
'''

lado1 = int(input("Digite um dos lados do triangulo: "))
lado2 = int(input("Digite um dos lados do triangulo: "))
lado3 = int(input("Digite um dos lados do triangulo: "))

perimetro = lado1+lado2+lado3

print(f"O perimetro do triangulo eh: {perimetro}")
